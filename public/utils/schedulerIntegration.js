import { createSchedulerTask, updateGoalTask } from "../db.js";

/**
 * Map a goalTask to a schedulerTask shape and persist it.
 * Updates the goalTask with the returned schedulerTaskId.
 * Returns the Firestore doc ref.
 */
export async function pushToScheduler(uid, goalTask) {
  // Map fields to schedulerTask schema
  const schedulerPayload = {
    title: goalTask.title,
    estimatedTime: goalTask.estimatedTime || 30,
    deadline: goalTask.deadline || null,
    priority: goalTask.priority || "medium",
    subject: "Personal Development",
    notes: `Auto-generated from goal: ${goalTask.sourceGoalId}`,
    status: "pending",
    // Keep the traceability link
    sourceGoalId: goalTask.sourceGoalId,
    sourceGoalTaskId: goalTask.id,
    autoGenerated: true,
  };

  const ref = await createSchedulerTask(uid, schedulerPayload);
  const schedulerTaskId = ref.id;

  // Mark the goalTask as scheduled and store the scheduler task ID
  await updateGoalTask(goalTask.id, {
    status: "scheduled",
    schedulerTaskId,
  });

  return ref;
}

/**
 * Push all pending (not yet scheduled) goal tasks to the Scheduler.
 * Returns array of pushed task refs.
 */
export async function pushAllPendingGoalTasks(uid, goalTasks) {
  const pending = goalTasks.filter(
    (t) => t.status === "pending" && !t.schedulerTaskId
  );

  const results = [];
  for (const task of pending) {
    try {
      const ref = await pushToScheduler(uid, task);
      results.push({ taskId: task.id, ref });
    } catch (err) {
      console.error("Failed to push goalTask to scheduler:", task.id, err);
    }
  }
  return results;
}
